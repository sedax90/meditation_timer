class BackgroundSound {
  final String title;
  final String asset;

  BackgroundSound({required this.title, required this.asset});

  @override
  String toString() {
    return title;
  }
}

class Speed {
  final String title;
  final double value;

  Speed({required this.title, required this.value});

  @override
  String toString() {
    return title;
  }
}

class BellSound {
  final String title;
  final String asset;

  BellSound({required this.title, required this.asset});

  @override
  String toString() {
    return title;
  }
}
